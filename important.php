                подготовлены надлежащим образом. <br />
                Для выезда из России за рубеж, как правило, требуется оформить
                заграничный паспорт. Тщательно проверьте правильность всех
                внесенных в паспорт сведений (фамилия, имя, отчество, дата
                рождения, пол, дата выдачи и срок действия паспорта, сведения о
                детях, включая их фотографии). Подробная информация о правилах
