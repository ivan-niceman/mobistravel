            <div class="accordion-content">
              <h3>ПУТЕШЕСТВИЯ С НЕСОВЕРШЕННОЛЕТНИМИ</h3>
              <p class="important-paragraph">
                Порядок выезда из Российской Федерации несовершеннолетних
                граждан Российской Федерации регламентирован положениями статьи
                20 Федерального закона от 15 августа 1996 г. № 114-ФЗ «О порядке
                выезда из Российской Федерации и въезда в Российскую Федерацию».
              </p>
              <p class="important-paragraph">
                В соответствии с положениями статьи указанной статьи
                несовершеннолетний гражданин Российской Федерации, как правило,
                выезжает из Российской Федерации, совместно хотя бы с одним из
                родителей, усыновителей, опекунов или попечителей.
              </p>
              <p class="important-paragraph">
                В случае выезда из Российской Федерации несовершеннолетнего
                гражданина Российской Федерации совместно с одним из родителей
                согласия на выезд ребенка за границу от второго родителя не
                требуется.
              </p>
              <p class="important-paragraph">
                Согласно разъяснению Минюста России срок действия нотариального
                согласия в соответствии со статьей 190 части первой Гражданского
                Кодекса Российской Федерации определяется календарной датой или
                истечением периода времени, который исчисляется годами,
                месяцами, неделями, днями или часами. Срок может определяться
                также указанием на событие, которое должно неизбежно наступить -
                совершеннолетие, окончание срока действия паспорта или визы и
                т.д.
              </p>
              <p class="important-paragraph">
                Поэтому нотариально заверенное согласие на выезд из Российской
                Федерации может быть выписано один раз вплоть до достижения
                ребенком совершеннолетия, при условии посещения им одних и тех
                же стран, указанных в этом согласии.
              </p>
              <p class="important-paragraph">
                Просим обратить внимание на то, что в согласии обязательно
                должны быть указаны названия государств, которые ребенок намерен
                посетить. При этом названия сообществ, типа страны Евросоюза,
                страны СНГ и т.д. не допускаются.
              </p>
              <p class="important-paragraph">
                Согласие родителей, оформленное нотариусом иностранного
                государства, должно содержать перевод на русский язык и быть
                соответствующим образом легализовано путем заверения его
                консульским загранучреждением МИД России или проставлением
                апостиля, предусмотренного Гаагской конвенцией, отменяющей
                требование легализации иностранных официальных документов от 5
                октября 1961 г.
              </p>
              <p class="important-paragraph">
                Для въезда в Российскую Федерацию несовершеннолетнего
                российского гражданина оформление нотариально удостоверенного
                согласия не требуется, т.к. в соответствии с положениями статьи
                2 Федерального закона от 15 августа 1996 г. № 114-ФЗ «О порядке
                выезда из Российской Федерации и въезда в Российскую Федерацию»
                гражданин Российской Федерации не может быть лишен права на
                въезд в Российскую Федерацию.
              </p>
              <p class="important-paragraph">
                Может ли ребенок выезжать из Российской Федерации, в случае если
                сведения о нем внесены в биометрические паспорта сопровождающих
                его родителей?
              </p>
              <p class="important-paragraph">
                В соответствии с требованиями постановления Правительства
                Российской Федерации от 19 января 2010 г. № 13 «О порядке
                использования бланков паспорта гражданина Российской Федерации,
                дипломатического паспорта гражданина Российской Федерации и
                служебного паспорта гражданина Российской Федерации,
                удостоверяющих личность гражданина Российской Федерации за
                пределами территории Российской Федерации, содержащих
                электронные носители информации», вступившего в силу 1 марта
                2010 г., внесение сведений о детях в паспорт (биометрический),
                удостоверяющий личность родителя не дает права ребенку на выезд
                за пределы территории Российской Федерации без своего документа,
                удостоверяющего личность гражданина Российской Федерации за
                пределами территории Российской Федерации.
              </p>
              <p class="important-paragraph">
                Может ли ребенок выезжать из Российской Федерации, в случае если
