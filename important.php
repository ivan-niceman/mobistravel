              <h3>КЛАССИФИКАЦИЯ ТИПОВ ПИТАНИЯ:</h3>
              <p class="important-paragraph">
                При описании туров используются сокращения, обозначающие
                предлагаемое питание. Хотя строгих стандартов на них нет,
                большинство отелей придерживаются единых правил на обозначения
                системы питания. Чаще всего предлагается питание на основе BB,
                HB, FB или AL, но встречаются и другие варианты. При этом
                следует помнить, что при одинаковых принципах питания набор блюд
                в пятизвездочном отеле будет заведомо лучше, чем в
                трехзвездочном.
              </p>
              <p class="important-paragraph">
                <b>BB – Bed & Breakfast (кровать и завтрак)</b>
              </p>
              <p class="important-paragraph">
                Питание BB означает, что кроме проживания вам предложат только
                завтрак. Дополнительное питание возможно за отдельную плату, как
                в ресторанах отеля, так и в любых других ресторанах курорта. В
                недорогих гостиницах часто предлагается Continental Breakfast
                (континентальный завтрак) – легкий завтрак из кофе или чая,
                сока, булочки, масла и джема. Возможно, на завтрак дадут также и
                тонкие куски сыра или колбасы. Если предлагается Buffet
                Breakfast (буфет), известный у нас, как «шведский стол», на
                завтрак также предлагаются несколько видов салатов и выпечки,
                блюда из яиц. В отелях высокой категории на завтрак могут
                подавать и горячие блюда. Следует помнить, что практически во
                всех курортных отелях завтрак входит в стоимость проживания и
                отказаться от него нельзя.
              </p>
              <p class="important-paragraph">
                Питание по типу BB чаще всего предлагается для экскурсионных
                туров или деловых поездок, иногда для горнолыжного отдыха, хотя
                на некоторых курортах этот вариант достаточно распространен.
              </p>
              <p class="important-paragraph">
                <b>НВ – Нalf Board (полупансион)</b>
              </p>
              <p class="important-paragraph">
                Вариант HB предполагает завтрак и ужин, без обеда. Завтрак в
                этом случае чаще всего предлагается в виде шведского стола, а на
                ужин может предлагаться также шведский стол или салат-бар с
                горячими блюдами по выбору согласно меню. Напитки могут входить
                в стоимость проживания, а могут оплачиваться отдельно. Часто для
                расширенного полупансиона используют обозначение HB+ или ExtHB.
                В этом случае в течение всего дня могут бесплатно предлагаться
                безалкогольные и алкогольные напитки местного производства.
              </p>
              <p class="important-paragraph">
                Вариант питания НВ распространен на большинстве курортов, не
                считая Турции, где более популярен вариант AL. Разнообразие,
                обилие, вкус и качество блюд на завтраки и ужины могут сильно
                различаться в зависимости от отеля.
              </p>
              <p class="important-paragraph">
                <b>FB – Full Board (полный пансион)</b>
              </p>
              <p class="important-paragraph">
                Питание по системе FB предполагает полноценное трехразовое
                питание. Завтрак чаще всего предлагается в виде шведского стола,
                а обед и ужин могут быть как шведским столом, так и выбором блюд
                из меню. При этом некоторые напитки могут входить в стоимость, а
                некоторые могут оплачиваться отдельно. Иногда используется
                обозначение FB+ или ExtFB, описывающее расширенный пансион. В
                этом случае в стоимость включаются алкогольные и безалкогольные
                напитки местного производства.
              </p>
              <p class="important-paragraph">
                При выборе полного пансиона или полупансиона, часто выбирается
                последний, так как на курортах всегда можно найти качественные
                рестораны, где можно пообедать, не возвращаясь в отель. Кроме
                того, при выборе полупансиона всегда имеется возможность менять
                место обеда и знакомиться с разными кухнями.
              </p>
              <p class="important-paragraph">
                <b>
                  AL или AI – All Inclusive (все включено) и UAL или UAI – Ultra
                  All Inclusive (ультра все включено)
                </b>
              </p>
              <p class="important-paragraph">
                Вариант AL предполагает трехразовое питание и разнообразные бары
                и кафе, предлагающие в течение дня безалкогольные и алкогольные
                напитки местного производства, а также легкие закуски. Вариант
                UAL подразумевает расширенный вариант All Inclusive. Также такие
                варианты могут называться Ex all inc, VIP all inc, Superior all
                inc и так далее, при этом конкретное описание схемы питания
                можно узнать только в отеле.
              </p>
              <p class="important-paragraph">
                В большинстве случаев варианты UAL и аналогичные предполагают
                практически постоянное питание – ранний завтрак, завтрак,
