                медицинской помощи при необходимости) и станете полностью
                зависимы от вашего работодателя. Внимательно изучите предложение
                работодателя и проверьте правильность оформления документов на
                работу. Перед тем, как подписать договор, убедитесь, что все
                пункты ясны. В случае сомнений обратитесь к независимому юристу
                за консультацией. При выезде с целью трудоустройства убедитесь,
                что имеющийся у вас тип визы предусматривает возможность занятия
