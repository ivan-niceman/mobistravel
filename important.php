                можно отведать пиццу, мороженное, выпечку, десерты и выпить
                любые напитки, в том числе свежевыжатые соки и алкоголь
                импортного производства, хотя за элитные вина и коньяки чаще
                всего придется заплатить отдельно. Нередко вместо шведского
                стола можно сходить в небольшой ресторан, специализирующийся на
                какой-то национальной кухне.
              </p>
              <p class="important-paragraph">
