                включая время в движении, направление, пройденный путь,
                скорость, какие-либо ориентиры вдоль дороги, знаки и такие звуки
                как звон колоколов, голоса, шум стройки, железной дороги,
                трамвая и т.д. Стремление «угодить» зачастую неправильно
                воспринимается террористами и затрудняет вызволение жертв. Если
                террористы принуждают заложника сделать письменное или устное (в
                аудио- либо видеозаписи) обращение к властям с изложением их
                требований, делать это можно только в той форме и объеме, на
                которых настаивают похитители. При этом следует избегать
                собственных заявлений и оценок, поскольку это может усугубить
                положение захваченного.
                <br />
                При проведении специальной операции по освобождению заложника
                рекомендуется лечь на пол, не двигаться, пока не будут получены
