                различным формам эксплуатации. Преступные группы обычно
                используют для поиска потенциальных жертв подставные компании,
                чаще всего с помощью газетных объявлений о выгодной работе. Они
                могут скрываться под вывеской вполне законных организаций:
                агентств моды, бюро путешествий, центров по трудоустройству,
                международных служб знакомств и т.п.
                <br />
                При поиске работы за рубежом не соглашайтесь на предложения
                нелегальной подработки. В случае если вы будете работать
                нелегально, то не сможете получить достойную оплату труда,
