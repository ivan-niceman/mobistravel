                невозможным пересечение границы. Законы и правила многих
                иностранных государств предусматривают, что срок действия
                паспорта должен заканчиваться не ранее, чем через шесть месяцев
                после въезда в страну.
                <br />
                Во время пребывания за границей храните паспорт в надежном месте
                (например, в сейфе гостиницы), не отдавайте его никому в
                качестве залога. Если во время поездки паспорт будет утрачен,
                необходимо обратиться в российское дипломатическое
                представительство или консульское учреждение за свидетельством
                на въезд (возвращение) в Российскую Федерацию. Билеты должны
                быть выписаны на то же имя, которое указано в паспорте.
                Проверьте правильность указанных в них сведений (даты, маршрут).
                Рекомендуется сделать несколько копий билетов, страниц паспорта
                (как заграничного, так и внутреннего) с личными данными и
                имеющимися в нем отметками, водительского удостоверения,
                кредитных карт, турпутевки, приглашений, страхового полиса.
                Возьмите с собой фотографии, как свои, так сопровождающих членов
                семьи. Они пригодятся, например, в случае утери загранпаспорта.
                Хранить копии документов следует отдельно от их оригиналов.
                <br />
                Узнайте заранее, требуется ли виза в страну назначения. За
                детальной информацией о порядке получения визы рекомендуем
                обратиться в дипломатические представительства или консульские
                учреждения соответствующих иностранных государств. Если вы
                следуете транзитом через территорию других государств, то
                уточните, требуется ли для такой поездки транзитная виза. Особое
                внимание уделите оформлению медицинской страховки. Наличие
                полиса медицинского страхования, действительного на весь срок
                пребывания за границей, является обязательным условием для
                получения гражданами России виз и въезда на территорию ряда
                иностранных государств (например, стран Шенгенской зоны). Для
                въезда в некоторые страны даже в рамках безвизового режима
                (например, в Израиль) также нужен страховой полис.
                <br />
                Рекомендуется оформить медицинскую страховку в любом случае,
                даже если она не требуется для получения визы или въезда в
                страну. Отсутствие страховки может обернуться крупными расходами
                при наступлении страхового случая. Оформляя страховой полис,
                подробно узнайте, какие медицинские услуги вам обязаны
                предоставить в стране пребывания при наступлении страхового
                случая, также какова процедура их получения и оплаты (иногда
                нужно оплатить визит к врачу самостоятельно, затем на основании
                чеков и квитанций потраченные денежные средства возвращаются
                через страховую компанию).
                <br />
