                условиях не пытаться помогать спецназу в своем освобождении;
                исходить из того, что спецназ будет относиться к заложнику как к
                возможному террористу, пока не будет установлена его личность;
                оставаться законопослушным и терпимым к действиям спецназа, даже
                если в ходе операции к заложнику будут применены меры
