                выезда из Российской Федерации и въезда в Российскую Федерацию»
                в случае, если один из родителей, усыновителей, опекунов или
                попечителей заявит о своем несогласии на выезд из Российской
                Федерации несовершеннолетнего гражданина Российской Федерации,
                вопрос о возможности его выезда из Российской Федерации
                разрешается в судебном порядке.
              </p>
              <p class="important-paragraph">
