                руки). Сразу после освобождения желательно не давать
                комментариев средствам массовой информации до тех пор, пока не
                будет проведена беседа с официальным российским представителем,
                и от него не будут получены соответствующие рекомендации. Одним
                из самых разрушительных стихийных бедствий является
                землетрясение. Наиболее опасные места находятся вблизи т.н.
                мировых поясов землетрясений, которые расположены на
                тихоокеанском побережье Северной и Южной Америки; на восточном
                побережье Японии; в центральной части Тихого океана; по южной
                кромке Гималаев; в странах Карибского региона; в Новой Зеландии;
                в Греции, Турции, Италии, Средиземноморье и Центральной
                Атлантике. Большинство несчастных случаев — результат обрушения
                зданий и других объектов. Землетрясения могут вызвать оползни и
                гигантские океанские волны цунами (сейсмические морские волны),
                которые в состоянии причинить огромный ущерб.
                <br />
                Начальный момент землетрясения может ощущаться по-разному.
                Иногда перед землетрясением появляется свечение над
                возвышенностями, могут происходить нарушения в работе радио,
                телевидения, электронных приборов, самопроизвольное свечение
                люминесцентных ламп. Порой, за несколько секунд до
                землетрясения, под землей возникает сильный нарастающий гул,
                после которого происходит первый толчок. В других случаях,
                непосредственно перед толчком, могут возникнуть более слабые
                колебания, при которых начинает дребезжать посуда, раскачиваются
                подвешенные предметы. Затем возникает первый толчок, который
                может продолжаться от нескольких секунд до 1-1,5 минут.
                <br />
                Во время землетрясения не следует выбегать из здания, так как
                падающие обломки и разрушающиеся стены становятся основной
                причиной многих жертв. Необходимо дождаться окончания
                землетрясения, после чего можно покинуть здание. Ни в коем
                случае не пытайтесь выбраться из здания с помощью лифта, который
                может застрять или упасть в шахту. Безопасным местом могут быть
                проемы внутренних дверей и углы комнат. Необходимо отойти дальше
                от окон и внешних стен, а также громоздкой мебели. Если здание
                невысокое и не сейсмостойкое, например, кирпичный дом и имеется
                возможность немедленно покинуть его, то в этом случае необходимо
                осторожно и быстро выйти из здания и отбежать от него на
                безопасное расстояние. Не следует приближаться к линиям
                электропередач. Если вы находитесь в автомобиле, необходимо
                остановиться как можно дальше от высоких зданий и других
                сооружений и не начинать движения до прекращения землетрясения.
                <br />
                После землетрясения постарайтесь действовать следующим образом.
                Если землетрясение произошло ночью, вместо спичек или зажигалки
                лучше воспользоваться электрическим фонариком. Если это
                невозможно, то прежде чем использовать спички или зажигалки,
                убедитесь в отсутствии запаха газа, бензина и других
                воспламеняющихся и взрывоопасных веществ. В противном случае
                может произойти взрыв или возгорание. Рекомендуется перекрыть
                газ, воду и выключить электричество. Если имеются незначительные
                очаги возгорания, попытайтесь их локализовать своими силами.
                Если потушить пожар самостоятельно невозможно, следует вывести
                детей, а также раненых людей из зоны пожара в безопасное место.
                Нельзя прикасаться к оголенным проводам и соприкасающимся с ними
                металлическим и мокрым предметам. При обнаружении просыпавшихся
                или пролитых взрывоопасных и ядовитых веществ следует
                предупредить о них окружающих. По возможности включите
                радиоприемник для получения информации о масштабах катастрофы,
                предпринимаемых мерах по ликвидации ее последствий.
