                </li>
              </ul>
              <p class="important-paragraph">
                <b>Региональные особенности</b> <br /> <br />
                Принимая решение отправиться в иностранное государство, помните,
                что существующие в нем общественно-политическая обстановка,
                климатические условия, законы и обычаи, особенности поведения
                могут не совпадать с вашими представлениями об условиях
                идеального отдыха. Во многих странах жестко ограничено курение в
                общественных местах, ресторанах и барах, предусмотрен высокий
                штраф за выброс любого мусора в не предназначенных для этого
                местах. В условиях жаркого климата, способствующего
                возникновению опасных инфекционных заболеваний, необходимо
                строго соблюдать санитарно-гигиенические меры предосторожности.
                Настоятельно рекомендуется мыть фрукты и овощи
                мылом-антисептиком, употреблять воду, молоко и сок из фабрично
                закупоренных бутылок. Опасность для вашего здоровья могут
                представлять некипяченая вода и свежевыжатые соки; овощные и
                фруктовые салаты; мороженое; кондитерские изделия с фруктовой
                начинкой; пищевой лед.
                <br />
                Следует иметь в виду, что во многих мусульманских странах нормы
                поведения определяются законами шариата, которые строго
                регламентируют взаимоотношения мужчин и женщин, запрещают
                употребление алкоголя. Не забывайте о необходимости
                воздерживаться от приема пищи в общественных местах в светлое
                время суток во время Рамазана (месяц поста). Захватите с собой в
                поездку «строгий» гардероб (например, в Иране женщины должны
                появляться во всех общественных местах в платке, в ряде
                государств им запрещается появляться на людях в открытых
                одеждах).
                <br />
                Российским гражданам за границей следует уважать образ жизни
                местного населения, быть терпеливыми, не грубить, не повышать
                голоса, не проявлять высокомерия и пренебрежения к местной
                культуре, не допускать оскорбительных высказываний по отношению
                к руководителям страны пребывания.
                <br />
                <br />
                <b>Как избежать нежелательных инцидентов</b>
                <br />
                <br />
                Собирая чемоданы в поездку, учтите, что многие вещи могут быть
                запрещены к авиаперевозке, а для ручной клади действуют особые
                правила, которые лучше заранее уточнить в авиакомпании. Не
                теряйте талоны на сданный вами багаж, они понадобятся в случае
                его утраты. Имейте в виду, что, пересекая границу, вы
                столкнетесь с рядом формальностей при прохождении пограничного и
                таможенного контроля. Не забудьте заполнить таможенную
                декларацию, если это необходимо. Наиболее распространенные
                нарушения таможенных правил — несоблюдение запрета (или
                количественных ограничений) на ввоз и вывоз товаров,
                недостоверное или неполное декларирование провозимых через
                границу вещей. За такие нарушения может быть предусмотрен штраф,
                конфискация товаров или другие санкции.
                <br />
                При пересечении границы следует воздержаться от выполнения
