                Если вас захватили в заложники или похитили, не следует
                оказывать сопротивление. Рекомендуется выполнять первоначальные
                приказы террористов. Они могут оказаться людьми с неустойчивой
                психикой и повести себя непредсказуемо. Если вы почувствовали
