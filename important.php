                работу нужно оформлять до отъезда, обратившись в консульское
                учреждение соответствующего государства. Не следует доверять
                обещаниям, что можно въехать в страну по туристической визе, а
                все необходимые формальности будут улажены по прибытии в страну.
                Если вы будете работать, не оформив надлежащим образом
                документы, например, разрешение на работу, это будет нарушением
                законодательства страны пребывания, за которое могут
                оштрафовать, выслать из страны и закрыть въезд на длительный
                срок.
